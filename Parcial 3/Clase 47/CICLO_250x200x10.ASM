REPETICIONES EQU 0XC
TEMP EQU 0XD
FACTOR EQU 0XE
FACTORG EQU 0XF

BSF STATUS,RP0
CLRF TRISA
CLRF TRISB
BCF STATUS,RP0

;CUERPO DE RETRASO/*
INICIO
INCF PORTA,F
MOVLW D'10'
MOVWF FACTORG  ; VALOR DE CICLO GRUESO
LOOPG          ; LOOPG GRUESO
MOVLW D'200'
MOVWF FACTOR ; FACTOR X 200
LOOPM        ; LOOP MEDIO
CALL CICLO250
DECFSZ FACTOR,F
GOTO LOOPM
DECFSZ FACTORG  ; SE AGREGA DECREMENTO DEL LOOP GRUESO
GOTO LOOPG
GOTO INICIO

;PRIMER CICLO DE 250 MICROSEGUNDOS
CICLO250
MOVLW D'81'
MOVWF TEMP;1
LOOP
DECFSZ TEMP,F;1
