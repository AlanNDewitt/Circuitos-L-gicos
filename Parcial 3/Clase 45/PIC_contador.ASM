#INCLUDE "P16F84A.INC"
BSF STATUS,RP0;BIT DE BANCO para ir TRISA  TRISB
CLRF TRISB    ;LIMPIAR TRISB (A CERO) SALIDA PORTB
BSF TRISA,2	  ;Bit 2 de TRISA EN 1 HACE BIT 2 PORT ENTRADA
BCF STATUS,RP0 ;REGRESARA A BANCA 0 O PAGO
CICLO:
BTFSS PORTA,2  ;Probar si bit 2 = 1, salta
GOTO CICLO
CICLO2:
BTFSC PORTA,2	;Probar si Bit 2 = 0 , salta
GOTO CICLO2
INCF PORTB,F
GOTO CICLO
END