#INCLUDE "P16F84A.INC"
DIVIDENDO EQU 0XC
DIVISOR EQU 0XD
COCIENTE EQU 0XE
MULTIPLICANDO EQU 0XF
MULTIPLICADOR EQU 0X10
PRODUCTO EQU 0X11
BINARIO EQU 0X12 
BSF STATUS,5;CONFIGURAR PUERTOS
MOVLW 0XFF
MOVWF TRISB

BCF TRISA,4
BCF STATUS,RP0;SALIR DE CONFIGURACIÓN
BCF PORTA,4
INICIO:
BTFSS PORTA,0
GOTO INICIO
INICIO2:
BTFSC PORTA,4
GOTO INICIO2
MOVF PORTB,W;W=PORTB
MOVWF BINARIO
;PROCESO A BCD NÚMERO ENTRE 10 Y
;COCIENTE*6+BINARIO
CLRF COCIENTE;
MOVWF DIVIDENDO;W=PORTB
MOVLW 0XA;W=DIVISOR
DIVIDIENDO:
INCF COCIENTE,F;
SUBWF DIVIDENDO,F
BTFSC STATUS,C;CUANDO SEA NEGATIVOS SALTA
GOTO DIVIDIENDO
DECF COCIENTE,F;
;MULTIPLICAR*6
MOVLW 0X6
MOVWF MULTIPLICANDO
MOVF COCIENTE,W;W=COCIENTE
MOVWF MULTIPLICADOR
CLRW
MULTIPLICAR:
ADDWF MULTIPLICANDO,W
DECFSZ MULTIPLICADOR,F
GOTO MULTIPLICAR
ADDWF BINARIO,W;SUMAR AL DATO INICIAL
;RESULTADO FINAL
BSF PORTA,4 
BSF STATUS,RP0
CLRF TRISB
BCF STATUS, RP0
MOVWF PORTB
FIN
GOTO FIN

END
